def build_c_print_helper_fallback_prompt(qname: str, type_body: str,
                                         kind: str) -> str:
    """Build a trivial-fallback prompt for a print helper that cannot be
    repaired.

    The trivial fallback prints only the type name as a placeholder, so the
    function compiles and downstream code that calls it still works.
    """
    fname = f"print_{qname.replace('::', '_')}"
    if kind == "enum":
        sig = f"static inline void {fname}(FILE *out, enum {qname} val)"
        body = f'    (void)val;\n    fprintf(out, "{qname}{{}}");'
    else:
        # struct/union
        prefix = "union" if kind == "union" else "struct"
        sig = (f"static inline void {fname}(FILE *out, "
               f"const {prefix} {qname} *obj)")
        body = (f'    (void)obj;\n    fprintf(out, "{qname}{{}}");')
    example = f"{sig} {{\n{body}\n}}"

    return (
        f"The previous print helper for type `{qname}` failed to "
        f"compile after multiple attempts. Write a trivial "
        f"print helper that simply prints the type name "
        f"\"{qname}\" as a literal placeholder.\n\n"
        f"TYPE DEFINITION:\n```c\n{type_body}\n```\n\n"
        f"The function MUST be ``static inline`` and use fprintf to write "
        f"to the FILE* parameter. For example:\n"
        f"```c\n{example}\n```\n\n"
        f"Return ONLY the print helper function definition "
        f"with NO explanation, NO markdown formatting, NO "
        f"additional text."
    )
